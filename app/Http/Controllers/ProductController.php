    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Product  $product
     * @return \Illuminate\Http\Response
     */
    public function destroy(Request $request,Product $product)
    {
        //
        $productForm = $request->only('product_id');
        $product = Product::with('invoices')->where('id',$productForm['product_id'])->first();
        $product->invoices[0]->products()->detach($productForm['product_id']);
        $hasTask = $product->invoices[0]->products()->exists();
        if(!$hasTask){
            $product->invoices[0]->delete();
        }
        $product->delete();
        return response(['status'=>0],200);
    }
